public class Extension{

    private int dlcId;
    private String dlcName;
    private double dlcPrice;

    public int getDlcId() {
        return dlcId;
    }

    public void setDlcId(int dlcId) {
        this.dlcId = dlcId;
    }

    public String getDlcName() {
        return dlcName;
    }

    public void setDlcName(String dlcName) {
        this.dlcName = dlcName;
    }

    public double getDlcPrice() {
        return dlcPrice;
    }

    public void setDlcPrice(double dlcPrice) {
        this.dlcPrice = dlcPrice;
    }
}
